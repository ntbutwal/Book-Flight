package com.flight.DtoFlight;

public class DtoFlightBook {

	private String trip;
	private String from;
	private String to;
	private String dtoairlines;
	private double price;

	public String getTrip() {
		return trip;
	}

	public void setTrip(String trip) {
		this.trip = trip;
	}


	public String getFrom() {
		return from;
	}

	public void setFrom(String from) {
		this.from = from;
	}

	public String getTo() {
		return to;
	}

	public void setTo(String to) {
		this.to = to;
	}

	public String getDtoairlines() {
		return dtoairlines;
	}

	public void setDtoairlines(String dtoairlines) {
		this.dtoairlines = dtoairlines;
	}

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}

}
